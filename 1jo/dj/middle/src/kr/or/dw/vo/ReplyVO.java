	public String getRe_wdt() {
		return re_wdt;
	}
	public void setRe_wdt(String re_wdt) {
		this.re_wdt = re_wdt;
	}
	public String getGb_del() {
		return gb_del;
	}
	public void setGb_del(String gb_del) {
		this.gb_del = gb_del;
	}
	public String getStaff_id() {
		return staff_id;
	}
