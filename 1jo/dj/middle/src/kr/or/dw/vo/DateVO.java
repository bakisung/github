package kr.or.dw.vo;

public class DateVO {
	private int year;
	private int term;
	private String reg_start;
	private String reg_end;
	private String lec_start;
	private String lec_end;
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getTerm() {
		return term;
	}
	public void setTerm(int term) {
		this.term = term;
	}
	public String getReg_start() {
		return reg_start;
	}
	public void setReg_start(String reg_start) {
		this.reg_start = reg_start;
	}
	public String getReg_end() {
		return reg_end;
	}
	public void setReg_end(String reg_end) {
		this.reg_end = reg_end;
	}
	public String getLec_start() {
		return lec_start;
	}
	public void setLec_start(String lec_start) {
		this.lec_start = lec_start;
	}
	public String getLec_end() {
		return lec_end;
	}
	public void setLec_end(String lec_end) {
		this.lec_end = lec_end;
	}
	
	
	
}
